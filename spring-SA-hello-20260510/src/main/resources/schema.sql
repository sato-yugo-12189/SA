-- テーブル削除
DROP TABLE IF EXISTS categories;
DROP TABLE IF EXISTS items;

-- カテゴリーテーブル
CREATE TABLE categories
(
   id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
   name TEXT
);
-- 商品テーブル
CREATE TABLE items
(
   id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
   category_id INTEGER,
   name TEXT,
   price INTEGER
);